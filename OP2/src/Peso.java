public class Peso {
    public double kilosALibras(double kilos){
        return kilos * 2;
    }
    public double kilosAGramos(double kilos){
        return kilos * 1000;
    }
    public double LibrasAKilos(double libras){
        return libras / 2;
    }
    public double LibrasAGramos(double libras){
        return libras * 500;
    }
    public double gramosAKilos(double gramos){
        return gramos / 1000;
    }
    public double gramosALibras(double gramos){
        return gramos / 500;
    }
}
